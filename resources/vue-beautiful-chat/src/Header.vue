<template>
  <div class="sc-header" :style="{background: colors.header.bg, color: colors.header.text}">
    <slot>
      <img v-if="imageUrl" class="sc-header--img" :src="imageUrl" alt="" @click="toggleUserList"/>
      <div v-if="!disableUserListToggle" class="sc-header--title ">
        {{ title }}
      </div>
      <div v-else class="sc-header--title">{{ title }}</div>
    </slot>
    <v-tooltip v-model="showTooltip" top :open-delay="250">
      <template v-slot:activator="{on, attrs}">
        <div
          v-show="showChangeContextButton"
          class="sc-header--close-button"
          v-bind="attrs"
          v-on="on"
          @click="toggleTooltip"
        >
          <v-icon color="white" large>mdi-swap-horizontal-bold</v-icon>
        </div>
      </template>
      <span>{{ changeContextTooltip }}</span>
    </v-tooltip>
  </div>
</template>

<script>
import CloseIcon from './assets/close-icon-big.png'

export default {
  props: {
    icons: {
      type: Object,
      default: function () {
        return {
          close: {
            img: CloseIcon,
            name: 'default'
          }
        }
      }
    },
    imageUrl: {
      type: String,
      required: true
    },
    title: {
      type: String,
      required: true
    },
    changeContext: {
      type: Function,
      required: true
    },
    changeContextTooltip: {
      type: String,
      required: false
    },
    colors: {
      type: Object,
      required: true
    },
    disableUserListToggle: {
      type: Boolean,
      default: false
    },
    showChangeContextButton: {
      type: Boolean,
      default: false
    },
  },
  data() {
    return {
      inUserList: false,
      showTooltip: false
    }
  },
  methods: {
    toggleUserList() {
      this.inUserList = !this.inUserList
      this.$emit('userList', this.inUserList)
    },
    toggleTooltip() {
        this.changeContext()
    }
  },
  watch: {
    changeContextTooltip: function (newValue, oldValue) {
        if (newValue !== oldValue) {
            this.showTooltip = false;
        }
    }
  }
}
</script>

